    private void btnRespaldarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRespaldarActionPerformed
        try {
            String archivo = null;
            JFC_Salvar_Backup.setVisible(true);
            int result = JFC_Salvar_Backup.showSaveDialog(null);
            if (result == JFileChooser.APPROVE_OPTION) {
                archivo = JFC_Salvar_Backup.getSelectedFile().toString().concat(".sql");

                File file = new File(archivo);

                if (file.exists()) {
                    Object[] options = {"Si", "No"};
                    int opcao = JOptionPane.showOptionDialog(null, "Este archivo ya existe.¿Quiere sobre escribir este Archivo?", "¡¡¡Atención!!!",
                            JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
                    if (opcao == JOptionPane.YES_OPTION) {
                        Runtime bck = Runtime.getRuntime();
                        bck.exec("C:/xampp/mysql/bin/mysqldump.exe --host=localhost --user=root --password= --result-file=" + archivo + " --databases alquilerautos");
                        mensaje = "Backup realizado correctamente";
                        realizado();
                        dispose();
                    } else {
                        btnRespaldarActionPerformed(evt);//evento del boton o del label
                    }
                } else {
                    Runtime bck = Runtime.getRuntime();
                    bck.exec("C:/xampp/mysql/bin/mysqldump.exe --host=localhost --user=root --password= --result-file=" + archivo + " --databases puntodeventa");
                    mensaje = "Backup realizado correctamente";
                    realizado();
                    dispose();
                }
            }
        } catch (HeadlessException | IOException e) {
            JOptionPane.showMessageDialog(null, e, "Error!", 2);
        }
    }//GEN-LAST:event_btnRespaldarActionPerformed

    private void btnRestaurarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRestaurarActionPerformed
        // TODO add your handling code here:
        String archivo = "";
        JFC_Restaurar_Backup.setVisible(true);
        int result = JFC_Restaurar_Backup.showOpenDialog(null);
        if (result == JFileChooser.APPROVE_OPTION) {
            archivo = JFC_Restaurar_Backup.getSelectedFile().toString();

            File file;
            file = new File(archivo);
            path = file.getAbsolutePath();
            String dbUserName = "root";// username
            String dbPassword = "";//Password

            String[] restoreCmd = new String[]{"C:/xampp/mysql/bin/mysql.exe ", "--user=" + dbUserName, "--password=" + dbPassword, "-e", "source " + path};
            Process runtimProcess;
            try {
                runtimProcess = Runtime.getRuntime().exec(restoreCmd);
                int proceCom = runtimProcess.waitFor();

                if (proceCom == 0) {
                    mensaje = "Restauracion de BD exitosa";
                    realizado();
                } else {
                    mensaje = "Error al restaurar la BD";
                    fallo();
                }
            } catch (HeadlessException | IOException | InterruptedException e) {
            }
        }
    }//GEN-LAST:event_btnRestaurarActionPerformed

