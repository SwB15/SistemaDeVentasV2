    public void guardar() {
        if (txtSaldoApertura.getText().isEmpty() || Integer.parseInt(txtSaldoApertura.getText().replace(".", "")) < 0) {
            mensaje = "El monto debe ser mayor a 0";
            advertencia();
        }

        datos.setFechapertura(txtFecha.getText());
        datos.setHoraapertura(txtHora.getText());
        datos.setSaldoapertura(Integer.parseInt(txtSaldoApertura.getText().replace(".", "")));
        datos.setSaldocierre(0);
        datos.setFechacierre("ABIERTO");
        datos.setHoracierre("ABIERTO");

        if (funcion.insertar(datos, funcion.buscarUsuario(Principal3.lblUsuario.getText()))) {
            caja();

            mensaje = "Caja abierta correctamente";
            realizado();

            Principal3.smnuMovimientos.setEnabled(true);
            funcion.idCaja();
            funcion.idDetalleCaja();
            Principal3.txtIdcaja.setText(String.valueOf(funcion.idCaja()));
            Principal3.txtIddetallecaja.setText(String.valueOf(funcion.idDetalleCaja()));
            Principal3.txtCaja.setText("Caja Abierta");
            Principal3.smnuAbrirCaja.setEnabled(false);
            Principal3.smnuCerrarCaja.setEnabled(true);
            Principal3.mnuCompras.setEnabled(true);
            Principal3.mnuVentas.setEnabled(true);

            String usuario = Principal3.lblUsuario.getText();
            String objeto = txtSaldoApertura.getText();
            String Accion = "APERTURA";
//            audi.audiclientes(usuario, objeto, Accion);

            dispose();
        } else {
            mensaje = "Error al abrir la caja";
            fallo();
        }
    }
