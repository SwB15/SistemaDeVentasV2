    private int caja() {
        String sql = "SELECT MAX(idcaja)As caja FROM caja";
        try (Connection cn = DataSource.getConnection()){
            Statement st = cn.createStatement();
            rs = st.executeQuery(sql);
            while (rs.next()) {
                idcaja = rs.getInt("caja");
            }
            return idcaja;
        } catch (SQLException e) {
            return 0;
        }
    }

