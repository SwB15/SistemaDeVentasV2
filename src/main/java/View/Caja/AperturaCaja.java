    public void fechaHora() {
        fecha = new SimpleDateFormat("dd/MM/yyyy");
        fechaActual = fecha.format(date);

        hora = new SimpleDateFormat("HH:mm:ss");
        horaActual = hora.format(date);
    }

    private int cargarSaldoAnterior() {
        int saldo_anterior = 0;
        try (Connection cn = DataSource.getConnection()){
            String sql = "SELECT saldocierre FROM caja WHERE idcaja = (SELECT MAX(idcaja) FROM caja)";
            Statement st = cn.createStatement();
            rs = st.executeQuery(sql);
            if (rs.next()) {
                saldo_anterior = rs.getInt(1);
            }
        } catch (SQLException ex) {
            Logger.getLogger(AperturaCaja.class.getName()).log(Level.SEVERE, null, ex);
        }
        return saldo_anterior;
    }

